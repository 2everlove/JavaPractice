
public class ForWhileDowhile {

	public static void main(String[] args) {
		
		int i=0;
		
		do {
			i++;
			System.out.println(i);
		} while(i<5);
		//--
		System.out.println("*");
		for(int b=1; b<=5; b++) {
			System.out.println(b);
		}
		//
		System.out.println("-");
		int a=1;
		
		while(a<=5) {
			System.out.println(a);
			a++;
		}
	}

}
