package variable;

public class Sample02 {

	public static void main(String[] args) {
		// 청바지 구매 예제
		int jean = 0; //정수형 변수는 기본 0으로 초기화 함.
		
		//청바지 1개 증가(구매)
		jean = jean +1;
		
		//1개 더 추가구매
		jean = jean + 1;
		

		
		
		System.out.println("청바지 " + jean + "벌");
		System.out.println("구매 완료!!");

	}

}
