package classpart;

public class Calculator {

	//사칙연산 method만들기
	public int add(int n1, int n2) { //더하기
		return n1 + n2;
	}
	public int sub(int n1, int n2) { //뺴기
		return n1 - n2;
	}
	public int mul(int n1, int n2) { //곱하기
		return n1 * n2;
	}
	public int div(int n1, int n2) { //나누기
		return n1 / n2;
	}
}
